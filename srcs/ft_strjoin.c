#include <string.h>
#include <stdlib.h>
#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	i;
	size_t	j;
	size_t	full_len;
	char	*s;

	if (!s1 || !s2)
		return (NULL);
	i = 0;
	j = 0;
	full_len = (ft_strlen(s1) + ft_strlen(s2));
	s = malloc (full_len + 1);
	if (!s)
		return (NULL);
	while (s1[i])
	{
		s[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		s[i + j] = s2[j];
		j++;
	}
	s[i + j] = '\0';
	return (s);
}
